export function translate(key, language = 'tr') {
  if (!key) return '';
  const normalizedKey = key.toLowerCase().trim();
  
  if (translations[normalizedKey]) {
      return translations[normalizedKey][language] || translations[normalizedKey]['en'];
  }

  // "full_sun_part_shade" gibi karmaşık verileri parçalayıp çevirme
  if (normalizedKey.includes('_')) {
      const parts = normalizedKey.split('_');
      // İlk 2 parçayı dene (örn: full_sun)
      const simpleKey = parts.slice(0, 2).join('_'); 
      if (translations[simpleKey]) {
          return translations[simpleKey][language] || translations[simpleKey]['en'];
      }
  }

  return key.charAt(0).toUpperCase() + key.slice(1).replace(/_/g, ' ');
}