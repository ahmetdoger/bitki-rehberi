// Çeviri fonksiyonumuz
export function translate(key, language = 'tr') {
  if (!key) return '';
  
  // API'den gelen key'i standartlaştır (küçük harf)
  const normalizedKey = key.toLowerCase().trim();

  // Sözlükte bu anahtar var mı ve istenen dilde karşılığı var mı?
  if (translations[normalizedKey] && translations[normalizedKey][language]) {
    return translations[normalizedKey][language];
  }

  // Bulamazsa İngilizceye bak, o da yoksa key'i düzeltip göster
  if (translations[normalizedKey] && translations[normalizedKey]['en']) {
      return translations[normalizedKey]['en'];
  }

  // Hiçbir şey yoksa, ham key'i biraz süsleyip göster (örn: "rare_plant" -> "Rare plant")
  return key.charAt(0).toUpperCase() + key.slice(1).replace(/_/g, ' ');
}